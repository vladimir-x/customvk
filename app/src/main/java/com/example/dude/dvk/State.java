package com.example.dude.dvk;

public class State {

    String playTime;
    String patchMode;
    String patchFlag;
    String cashedJsCount;

    public String getPlayTime() {
        return playTime;
    }

    public void setPlayTime(String playTime) {
        this.playTime = playTime;
    }

    public String getPatchMode() {
        return patchMode;
    }

    public void setPatchMode(String patchMode) {
        this.patchMode = patchMode;
    }

    public String getPatchFlag() {
        return patchFlag;
    }

    public void setPatchFlag(String patchFlag) {
        this.patchFlag = patchFlag;
    }

    public String getCashedJsCount() {
        return cashedJsCount;
    }

    public void setCashedJsCount(String cashedJsCount) {
        this.cashedJsCount = cashedJsCount;
    }
}
